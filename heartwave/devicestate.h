#ifndef DEVICESTATE_H
#define DEVICESTATE_H

enum DeviceState { HOME, SESSION_SELECT, SETTINGS, CHALLENGE_LEVEL, BREATH_PACER, LOGS, LOG, ACTIVE_SESSION, SESSION_END };

// HOME = Main menu screen
// SESSION_SELECT = Select session simulation screen
// SETTINGS = Settings select screen
// CHALLENGE_LEVEL = Set challenge level screen
// BREATH_PACER = Set breath pace screen
// LOGS = View all logs
// LOG = View single log details
// ACTIVE_SESSION = Active session recording
// SESSION_END = Summary display after session ends

#endif // DEVICESTATE_H
